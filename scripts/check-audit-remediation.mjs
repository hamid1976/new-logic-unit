import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const read = (file) => fs.readFileSync(path.join(root, file), 'utf8')
const assert = (condition, message) => {
  if (!condition) throw new Error(message)
}

const layout = read('src/app/layout.tsx')
const consent = read('src/components/ConsentManager.tsx')
const contact = read('src/app/contact/ContactForm.tsx')
const contactApi = read('src/app/api/contact/route.ts')
const sitemap = read('src/app/sitemap.ts')
const insights = read('src/app/insights/[slug]/page.tsx')
const navigation = read('src/components/layout/Navigation.tsx')
const redirects = read('next.config.js')
const source = fs.readdirSync(path.join(root, 'src'), { recursive: true })
  .filter((file) => typeof file === 'string' && /\.(tsx?|jsx?)$/.test(file))
  .map((file) => read(path.join('src', file)))
  .join('\n')

assert(layout.includes('<ConsentManager />'), 'Root layout must render the consent manager.')
assert(!layout.includes('gtag/js'), 'Direct gtag loading must be removed.')
assert(!layout.includes('microsoft-clarity'), 'Direct Clarity loading must be removed.')
assert(layout.includes("window.gtag('consent', 'default'"), 'Google consent mode must default analytics storage to denied.')
assert(layout.includes("analytics_storage: 'denied'"), 'Google analytics storage must be denied before consent.')
assert(consent.includes('logic-unit-nonessential-consent'), 'Consent choice must persist.')
assert(consent.includes("window.gtag?.('consent', 'update'"), 'Consent choices must update Google consent mode.')
assert(contactApi.includes('submitLeadToHubSpot'), 'Contact API must submit to HubSpot server-side.')
assert(!contactApi.includes('Simulated email sent successfully'), 'Contact API must not simulate lead success.')
assert(contactApi.includes('ignored: true'), 'Honeypot responses must be distinguishable from accepted HubSpot leads.')
assert(contact.includes("trackGtmEvent('generate_lead'"), 'Contact form must emit one terminal GTM lead event.')
assert(contact.includes('isTrackableLeadResult(result)'), 'Only confirmed HubSpot lead responses may emit generate_lead.')
assert(contact.includes('lead_id: result.leadId'), 'Lead analytics must include the non-PII lead identifier returned after HubSpot success.')
assert(!sitemap.includes('new Date()'), 'Sitemap must not reset last-modified dates on every build.')
assert(!sitemap.includes('/ecosystem/hulm-pos`'), 'Retired HULM route must not be in the sitemap.')
assert(!sitemap.includes('/ecosystem/titan-cmms`'), 'Retired Titan route must not be in the sitemap.')
assert(!insights.includes('const breadcrumbSchema'), 'Insight page must not emit a duplicate breadcrumb graph.')
assert(navigation.includes('aria-expanded={mobileOpen}'), 'Mobile navigation must expose expanded state.')
assert(navigation.includes('aria-controls="mobile-navigation"'), 'Mobile navigation must identify its controlled region.')
assert((redirects.match(/nova-nordisk-sample-ordering-application/g) || []).length === 1, 'Novo redirect must be declared once.')
assert(redirects.includes("source: '/ecosystem/hulm-pos'"), 'Retired HULM route must redirect.')
assert(redirects.includes("source: '/ecosystem/titan-cmms'"), 'Retired Titan route must redirect.')
assert(!/Download The Playbook|Download Case Study Pack|Get The Checklist/.test(source), 'Unverified downloadable resources must not be promised.')
assert(fs.existsSync(path.join(root, 'src/app/opengraph-image.tsx')), 'Root OG image route must exist.')
assert(fs.existsSync(path.join(root, 'src/app/insights/[slug]/opengraph-image.tsx')), 'Insight OG image route must exist.')

const insightsData = read('src/data/insights.ts')
const hubspotServer = read('src/lib/hubspot-server.ts')

assert(contact.includes("'Investor relations'") && contact.includes("'Talent inquiry'"), 'ContactForm must have Investor relations and Talent inquiry options.')
assert(contact.includes('required={isProductInquiry}'), 'ContactForm interest field must be conditionally required on product inquiries.')
assert(insightsData.includes('relatedSlugs:'), 'insights.ts must contain explicit relatedSlugs maps instead of dynamic array slicing.')
assert(!insights.includes('cta=checklist'), 'Insights page should not promise checklist downloads.')
assert(hubspotServer.includes("name: 'lu_landing_page_url'"), 'HubSpot payload must map lu_landing_page_url.')
assert(hubspotServer.includes("name: 'lu_referrer_url'"), 'HubSpot payload must map lu_referrer_url.')
assert(contact.includes('trackedLeads.includes'), 'Lead submission tracking must include deduplication logic.')
assert(!contact.includes("email: payload.email") || !contact.includes("trackGtmEvent('generate_lead', { email"), 'GTM analytics events must not contain PII like email addresses.')

console.log('Audit-remediation source checks passed.')
